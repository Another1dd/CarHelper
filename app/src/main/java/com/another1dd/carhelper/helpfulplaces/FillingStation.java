package com.another1dd.carhelper.helpfulplaces;

public class FillingStation {
    public String name;
    public String text;


    public FillingStation(){}

    public FillingStation(String name, String text)
    {
        this.name = name;
        this.text = text;

    }

    public String getName() {
        return name;
    }

    public String getText() {
        return text;
    }


}
